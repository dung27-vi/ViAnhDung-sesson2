#include <stdio.h>

int main() {
    int chieu_dai = 10;
    int chieu_rong = 5;

    int chu_vi = (chieu_dai + chieu_rong) * 2;
    int dien_tich = chieu_dai * chieu_rong;

    printf("Chu vi hinh chu nhat la: %d\n", chu_vi);
    printf("Dien tich hinh chu nhat la: %d\n", dien_tich);

    return 0;
}
